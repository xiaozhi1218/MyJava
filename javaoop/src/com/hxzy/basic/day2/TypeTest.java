package com.hxzy.basic.day2;

/**
 * 类型转换测试以及计算
 */
public class TypeTest {
    public static void main(String[] args) {
        double a = 3.14;
        System.out.println(a);
        float b = 3.14f;
        System.out.println(b);
        double c = 5.0;
        int d = -4;
        System.out.println(c / d);
    }
}


